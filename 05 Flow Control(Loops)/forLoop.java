import java.util.*;

// logic for the for loop
public class forLoop {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        for(int i=0; i<=n; i++){
            System.out.println("Hello World");
        }
        System.out.println("Printed " + n + " Times");
    }
}
